#include <stdio.h>

int main()
{
    int x = 10;
    char c = 'Y';
    float f = 8.123456789;
    double d = 7.123456789;

    printf("Integer is %d\n", x);
    printf("Character %c and its ASCII value is %d\n", c, c);
    printf("Float is %f\n", f);
    printf("Double is %lf\n", d);
    return 0;
}